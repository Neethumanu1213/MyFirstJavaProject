package learningCollections;

public class Students {
	private String name;
	private String rollNum;
	public Students(String name, String rollNum) {
		super();
		this.name = name;
		this.rollNum = rollNum;
	}
	public String getName() {
		return name;
	}
	public String getRollNum() {
		return rollNum;
	}
	
	
}

